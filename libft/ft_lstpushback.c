#include "libft.h"

void	ft_lstpushback(t_list **alst, t_list *new)
{
	t_list	*last;

	if (!alst)
		return ;
	if (!(*alst))
	{
		*alst = new;
		return ;
	}
	last = *alst;
	while (last->next)
		last = last->next;
	last->next = new;
}
